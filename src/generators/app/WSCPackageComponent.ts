/**
 * Represents a component of the `WSCPackageGenerator`.
 */
export enum WSCPackageComponent
{
    /**
     * Indicates `Files`.
     */
    Files = "files",

    /**
     * Indicates `CronJobs`.
     */
    CronJobs = "cronJobs",

    /**
     * Indicates a `PHPScript`.
     */
    PHPScript = "phpScript",

    /**
     * Indicates an `SQLScript`.
     */
    SQLScript = "sqlScript",

    /**
     * Indicates `Translations`.
     */
    Translations = "translations",

    /**
     * Indicates `ErrorMessages`.
     */
    ErrorMessages = "errorMessages",

    /**
     * Indicates `ACPOptions`.
     */
    ACPOptions = "acpOptions",

    /**
     * Indicates `UserOptions`.
     */
    UserOptions = "userOptions",

    /**
     * Indicates `GroupOptions`.
     */
    GroupOptions = "groupOptions",

    /**
     * Indicates `Themes`.
     */
    Themes = "themes",

    /**
     * Indicates `Emojis`.
     */
    Emojis = "emojis",

    /**
     * Indicates `BBCodes`.
     */
    BBCodes = "bbCodes",

    /**
     * Indicates `Templates`.
     */
    Templates = "templates",

    /**
     * Indicates `ACPTemplates`.
     */
    ACPTemplates = "acpTemplates",

    /**
     * Indicates `EventListeners`.
     */
    EventListeners = "eventListeners",

    /**
     * Indicates `TemplateListeners`.
     */
    TemplateListeners = "templateListeners"
}
