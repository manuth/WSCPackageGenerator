/**
 * Represents a component of the `WSCThemeGenerator`.
 */
export enum WSCThemeComponent
{
    /**
     * Indicates `SCSSCode`.
     */
    SCSSCode = "customScss",

    /**
     * Indicates `SCSSOverrides`.
     */
    SCSSOverrides = "scssOverride",

    /**
     * Indicates `Variables`.
     */
    Variables = "variables"
}