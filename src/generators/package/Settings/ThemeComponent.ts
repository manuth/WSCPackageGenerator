/**
 * Represents a component of a theme.
 */
export enum ThemeComponent
{
    /**
     * Indicates custom scss-code.
     */
    CustomScss = "customScss",

    /**
     * Indicates scss variable overrides.
     */
    ScssOverrides = "scssOverrides",

    /**
     * Indicates custom variables.
     */
    Variables = "variables"
}
